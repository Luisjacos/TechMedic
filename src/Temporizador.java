public class Temporizador {

    private int segundos;
    private int minutos;
    private int horas;

    public int getSeconds() {
        return segundos;
    }
    
    public int getMinutes() {
        return minutos;
    }
    
    public int getHoras() {
        return horas;
    }
    
    public void setSeconds(int s) {
        segundos = s;
    }
    
    public void setMinutes(int m) {
        minutos = m;
    }
    
    public void setHoras(int h) {
        horas = h;
    }
}
